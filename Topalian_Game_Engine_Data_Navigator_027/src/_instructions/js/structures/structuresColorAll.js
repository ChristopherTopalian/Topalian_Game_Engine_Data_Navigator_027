// Color All Structures

function structuresColorAll()
{
    let theStructure;

    for (let x = 1; x <= structuresArray.length; x++)
    {   
        theStructure = "STRUCTURE" + x;

        ge(theStructure).style.backgroundColor = "rgb(70, 95, 140)";
    }
}

